int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n-- > 1 && *s1 == *s2 && *s1 != '\0' && *s2 != '\0')
		return (ft_strncmp(s1 + 1, s2 + 1, n));
	if (*s1 == *s2)
		return (0);
	if (*s1 != *s2)
		return (*s1 - *s2);
	return (0);
}
