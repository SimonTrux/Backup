#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	char	*str;
//	char	uc;

//	uc = (unsigned char)c;
	str = (void*)s;
	i = 0;
	if (n == 0 || s == NULL)
		return (NULL);
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return (&str[i]);
		i++;
	}
	return (NULL);
}
